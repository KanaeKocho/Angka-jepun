#include <iostream>
using namespace std;
int main(){
    int angka;
    cout<< "=== Angka Jepang ==="<<endl;
    cout<<"Masukkan Angka 0 - 10"<<endl;
    cin>>angka;
    if(angka == 0){
        cout<<"零 (Zero)"<<endl;
    }
    if(angka == 1){
        cout<<"一 (Ichi)"<<endl;
    }
    if(angka == 2){
        cout<<"二 (Ni)"<<endl;
    }
    if(angka == 3){
        cout<<"三 (San)"<<endl;
    }
    if(angka == 4){
        cout<<"四 (Yan / Shi)"<<endl;
    }
    if(angka == 5){
        cout<<"五 (Go)"<<endl;
    }
    if(angka == 6){
        cout<<"六 (Roku)"<<endl;
    }
    if(angka == 7){
        cout<<"七 (Nana)"<<endl;
    }
    if(angka == 8){
        cout<<"八 (Hachi)"<<endl;
    }
    if(angka == 9){
        cout<<"九 (Kyuu)"<<endl;
    }
    if(angka == 10){
        cout<<"十 (Juu)"<<endl;
    }
}
